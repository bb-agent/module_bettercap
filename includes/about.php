<b>BetterCap</b> by Simone 'evilsocket' Margaritelli
<br>
<br>BetterCAP is a powerful, flexible and portable tool created to perform various types of <b>MITM</b> attacks against a network, manipulate <b>HTTP</b>,
<b>HTTPS</b> and <b>TCP</b> traffic in realtime, sniff for credentials and much more.
